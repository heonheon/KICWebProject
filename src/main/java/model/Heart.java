package model;

public class Heart {
	int num;
	String id;
	int heart;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getHeart() {
		return heart;
	}
	public void setHeart(int heart) {
		this.heart = heart;
	}
	@Override
	public String toString() {
		return "Heart [num=" + num + ", id=" + id + ", heart=" + heart + "]";
	}
}
